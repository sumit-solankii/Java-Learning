public class variables {
    
    public static void main(String[] args) {
        //variables-->
        int i = 5;
        System.out.println("i is " + i); // i is 5

        byte b = 10;
        System.out.println("b is " + b + 10); // b is 15

        b = (byte)i;
        System.out.println("b is now " + 5); // b is now 5

    }
}
